    public void Get_Set_Id(){
//        
         try{
                    str=con.prepareStatement("SELECT AUTO_INCREMENT FROM information_schema.TABLES WHERE TABLE_NAME = 'items' ");
                    rs=str.executeQuery();
                    if (rs.next()){
                        int id=rs.getInt("AUTO_INCREMENT");
                        IDtxt.setText(""+id);
                    }
                    
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,ex,"error",2);
        }
        
    }
    
    
    public void get_SetData(){
      try{
          
          str = con.prepareStatement("Select* from items");
          rs=str.executeQuery();
          DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
          df.setRowCount(0);
          while(rs.next()){
            String [] row = {rs.getString("ID"),rs.getString("ITEM"),rs.getString("TYPE")};
            df.addRow(row);
          }
          
      }catch(Exception ex){
          JOptionPane.showMessageDialog(this, ex.getMessage());
      }
    }
    
    
    
    
    public boolean submit(){
        try{
            str = con.prepareStatement("INSERT INTO `items`(`ID`,`ITEM`,TYPE) VALUES (?,?,?)");
            str.setString(1, IDtxt.getText());
            str.setString(2, ITEMtxt.getText());
            str.setString(3, Type_cb.getSelectedItem().toString());
            str.executeUpdate();
            return true;
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(),"ERROR",2);
            return false;
        }
    }
    
    
    
    
    public boolean update(){
        try{
            String ID=IDtxt.getText();
            str = con.prepareStatement("UPDATE `items` SET `ITEM`=?,`TYPE`=? WHERE ID="+ID);
            str.setString(1, ITEMtxt.getText());
            str.setString(2, Type_cb.getSelectedItem().toString());
            str.executeUpdate();
            return true;
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(),"ERROR",2);
            return false;
        }
    }
    
    
    
    public boolean delete(){
       try{
            String ID=IDtxt.getText();
            str = con.prepareStatement("DELETE FROM `items` WHERE ID="+ID);
            str.executeUpdate();
            return true;
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(),"ERROR",2);
            return false;
        }
    }
    
    
    
    public boolean checkSideCase(){
        return IDtxt.getText().equals("") || ITEMtxt.getText().equals("");
    }
    
    
    public void tblClick(){
        DefaultTableModel df=(DefaultTableModel)jTable1.getModel();
        int srow = jTable1.getSelectedRow();
        IDtxt.setText(df.getValueAt(srow, 0).toString());
        ITEMtxt.setText(df.getValueAt(srow, 1).toString());
        Type_cb.setSelectedItem(df.getValueAt(srow, 2).toString());
        saveBtn.setText("UPDATE");    
    }
    
    
    
    public void Newbtn(){
        get_SetData();
        Get_Set_Id();
        ITEMtxt.setText("");
        Type_cb.setSelectedIndex(0);
        saveBtn.setText("SAVE");
        
        
    }
