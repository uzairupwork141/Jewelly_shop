    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        // TODO add your handling code here:
        if(checkSideCase()==true){
            JOptionPane.showMessageDialog(this, "empty fields","ERROR",2);
            return;
        }
        if(saveBtn.getText().equals("SAVE")){
            JOptionPane.showMessageDialog(this, "Unable to delete","ERROR",2);
            return;
        }
        int check = JOptionPane.showConfirmDialog(this, "Are you sure", "Deleting item", JOptionPane.YES_NO_OPTION);
        if(check==0){
            delete();
            Newbtn();
        }
        
    }//GEN-LAST:event_jLabel9MouseClicked

